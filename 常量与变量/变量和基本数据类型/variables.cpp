#include <iostream>

int main()
{
    int a = 10;
    std::cout << "a = " << a << std::endl;
    short b = 20;
    std::cout << "b = " << b << std::endl;
    long c = 30;
    std::cout << "c = " << c << std::endl;
    float d = 40.5;
    std::cout << "d = " << d << std::endl;
    double e = 50.5;
    std::cout << "e = " << e << std::endl;
    char f = 'A';
    std::cout << "f = " << f << std::endl;
    bool g = true;
    std::cout << "g = " << g << std::endl; 
    std::string h = "Hello World!";
    std::cout << "h = " << h << std::endl;
    return 0;
}